import numpy as np,pylab

fig=pylab.figure()
ax=fig.add_axes((0,0,1,1))
pylab.plot([0.5,0.6,0.6,0.5,0.5],[0,0,1,1,0],'k--')
pylab.plot([0,1],[0,0],'k',lw=10)
